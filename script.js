function shuffleRunes() {
    const arr = [...runes];
    for (let i = arr.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [arr[i], arr[j]] = [arr[j], arr[i]];
    }
    return arr;
}

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏∏‡πà‡∏°‡∏£‡∏π‡∏ô‡πÅ‡∏ö‡∏ö‡∏Å‡∏•‡∏±‡∏ö‡∏´‡∏±‡∏ß
function shuffleRunesWithReversed() {
    const arr = [...runes];
    for (let i = arr.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [arr[i], arr[j]] = [arr[j], arr[i]];
    }
    
    // ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Å‡∏≤‡∏£‡∏™‡∏∏‡πà‡∏°‡∏£‡∏π‡∏ô‡πÅ‡∏ö‡∏ö‡∏Å‡∏•‡∏±‡∏ö‡∏´‡∏±‡∏ß (‡∏õ‡∏£‡∏∞‡∏°‡∏≤‡∏ì 30% ‡∏Ç‡∏≠‡∏á‡∏£‡∏π‡∏ô‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î)
    const shuffledRunes = arr.map(rune => {
        const isReversed = Math.random() < 0.3; // 30% ‡πÇ‡∏≠‡∏Å‡∏≤‡∏™‡∏ó‡∏µ‡πà‡∏à‡∏∞‡∏Å‡∏•‡∏±‡∏ö‡∏´‡∏±‡∏ß
        return {
            ...rune,
            isReversed: isReversed
        };
    });
    
    return shuffledRunes;
}

